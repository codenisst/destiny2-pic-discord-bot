package ru.codenisst.discord2pic.vk.models.vkpost.body;

public class Size {

    private long height;
    private String url;
    private String type;
    private long width;

    public long getHeight() {
        return height;
    }

    public String getURL() {
        return url;
    }

    public String getType() {
        return type;
    }

    public long getWidth() {
        return width;
    }
}
